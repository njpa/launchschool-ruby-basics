# EX 1
# ====
# Write a method named `print_me` that prints 
# `"I'm printing within the method!"` when invoked.

# ANSWER
# ======
# The method definition will not take any arguments, and will simply
# invoke `puts` passing in the string `"I'm printing within the method"`.  

def print_me
  puts "I'm printing within the method!"
end

print_me

