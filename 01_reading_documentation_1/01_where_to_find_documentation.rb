# QUESTION
# ========
# Where can you find the most complete Ruby documentation?

# ANSWER
# ======
# Online APIs such as ruby-doc.org, docs.ruby-lang.org.

# NOTES
# =====
# There is no official online ruby documentation.  ruby-doc.org is 
# an effort by the Ruby community to document the language itself,
# the core API, the standard library API, and other useful documents.
# It is the most complete and up-to-date documentation available.
# The core API consists of all classes and modules that are available
# in ruby programs without having to import them.
# The standard library API consists of classes and modules provided by
# the ruby distribution that need to be imported using `require` (e.g.
# `Set`, `Date`, `JSON`, `YAML`, etc.).

